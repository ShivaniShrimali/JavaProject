package com.collectioDemo.entity;

public class ArrayListUser {
 private int fuel;
 private String type;
public ArrayListUser(int fuel, String type) {
	
	this.fuel = fuel;
	this.type = type;
}
public ArrayListUser() {
	
}
public int getFuel() {
	return fuel;
}
public void setFuel(int fuel) {
	this.fuel = fuel;
}
public String getType() {
	return type;
}
public void setType(String type) {
	this.type = type;
}

 
}
